public class SwitchStatement {
    public static void main(String[] args) {
        int number=2;
        switch(number) {
            case 1:System.out.println("MYSURU");
            break;
            case 2:System.out.println("BENGALURU");break;
            case 3:System.out.println("CHIKKAMAGALUR");
            break;
            default :System.out.println("LONDON");
            break;
        }

    }

}
